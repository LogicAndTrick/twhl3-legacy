		<a href="http://ammahls.com/index.php?page=downloadcat&amp;cat=tools">Zoner's Half-Life Tools</a>(for compiling) &mdash; <span style="color: #ff0000">essential</span><br />

		Hammer comes with its own set of compile tools, but Zoner's are hugely better and are updated often. Comprehensive documentation is available <a href="http://www.zhlt.info">here</a>.
